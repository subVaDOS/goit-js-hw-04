const colors = [
  { hex: '#f44336', rgb: '244,67,54' },
  { hex: '#2196f3', rgb: '33,150,243' },
  { hex: '#4caf50', rgb: '76,175,80' },
  { hex: '#ffeb3b', rgb: '255,235,59' },
];

const hexColors = [];
const rgbColors = [];

for (const color of colors) {
  hexColors.push(color.hex); // Додаємо значення hex у масив hexColors
  rgbColors.push(color.rgb); // Додаємо значення rgb у масив rgbColors
}

console.log(hexColors); // ["#f44336", "#2196f3", "#4caf50", "#ffeb3b"]
console.log(rgbColors); // ["244,67,54", "33,150,243", "76,175,80", "255,235,59"]

const books = [
  { title: 'The Last Kingdom', author: 'Bernard Cornwell' },
  { title: 'Beside Still Waters', author: 'Robert Sheckley' },
  { title: 'The Dream of a Ridiculous Man', author: 'Fyodor Dostoevsky' },
];

const authorToSearchFor = 'Robert Sheckley';

for (const book of books) {
  if (book.author === authorToSearchFor) {
    console.log(book);
    console.log(book.title);
    console.log(book.rating);
  }
}

